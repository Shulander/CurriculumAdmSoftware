<h3>Processo seletivo 2008/2</h3>
<h3>Regulamento para Intercambistas</h3>
<ol>
<li>Inscrições
	<ul class="explicacao">
	<li>As inscrições serão feitas mediante cadastro online e participação obrigatória na palestra apresentação;</li>
	<li>Não serão aceitas inscrições que excedam o período determinado;</li>
	</ul>
</li>
<li>Etapas
	<ul class="explicacao">
	<li>O Processo Seletivo será constituído por 3 (três) etapas:
		<ol class="etapas"> 
		<li><b>Palestra apresentação:</b> Todos os candidatos deverão assistir a palestra apresentação do Processo Seletivo, 
		que serão promovidas pela AIESEC Santa Maria ao longo do período de recrutamento.</li> 
		<li><b>Entrevista:</b> Os candidatos que participaram da etapa anterior alcançam a 2ª etapa e estarão na fase decisiva 
		do Processo Seletivo. Realizado por um corpo avaliador, a entrevista é a etapa que determina quais dos candidatos 
		serão definitivamente selecionados. A entrevista terá como foco diversos aspectos individuais observados 
		durante todo o processo, para uma análise mais criteriosa. </li> 
		<li><b>Treinamento de novos membros:</b> Todos os candidatos aprovados nas etapas anteriores participarão do 
		treinamento de novos membros, quando receberão maiores informações sobre a AIESEC em Santa Maria e no mundo, 
		bem como entenderão melhor seu papel dentro da organização. A ausência do candidato é eliminatória.</li>
		</ol>
	</li>
	</ul>
</li>
<li>Informações Gerais
	<ol class="etapas">  
		<li><b>Período de inscrições:</b> 11/09 até 23/09</li>
		<li><b>Valor da taxa de inscrição:</b> R$ 5,00 (cinco reais) a ser paga após a palestra de apresentação 
		para agendar sua entrevista.</li>
		<li><b>Datas e locais das palestras de apresentação</b> (escolha apenas <u>um</u> dos horários):  
		<ul>
			<li>Dia 24/09: 11h, na Faculdade de Comunicação, prédio 21, segundo andar, UFSM - Campus.</li>
			<li>Dia 24/09: 18h, no auditório do CCSH - UFSM, Centro.</li>
			<li>Dia 25/09: 11h, no auditório do CCSH - UFSM, Centro.</li>
		</ul>
		</li>
		<li><b>Local das entrevistas:</b> Escritório da AIESEC em Santa Maria (Rua Floriano Peixoto, 1184, 8° andar do 
		prédio do CCSH).
		<li><b>Local do treinamento de membros novos:</b> a confirmar.
	</ol>
</li>
<li>Eliminação:
	<ul class="explicacao">
	<li>Será considerado eliminado do Processo Seletivo 2008/2 o candidato que:
		<ul class="explicacao">
		<li>Fizer o cadastro online, preencher os seus dados mas esquecer de levar no dia da palestra de apresentação a sua 
		ficha impressa com foto 3x4 ou digitalizada;</li>
		<li>Não pagar a taxa de inscrição para a entrevista;</li>
		<li>Não for aprovado nas etapas das quais tenha participado;</li>
		<li>Por quaisquer motivos não comparecerem a uma das etapas do processo, caracterizando-se como 
		desistência (inclusive nas palestras).</li>
		</ul>
	</li>
	</ul>
</li>
<li>Resultados:
	<ul class="explicacao">
	<li>A AIESEC em Santa Maria informará o local e horário das etapas deste processo seletivo, bem como o 
	resultado aos candidatos aprovados em cada etapa, via e-mail cadastrado, bem como pela página de acesso do usuário.</li>
	<li>Os dias e locais das etapas variam, em caso de dúvida acesse a página principal do Processo Seletivo.</li>
	<li>Os locais e horários serão confirmados com antecedência aos candidatos aprovados, de acordo com as etapas.</li>
	<li>Caso alguma data seja alterada, por motivo extraordinário, os candidatos serão notificados com antecedência.</li>
	<li>O valor e o local do treinamento de novos membros serão divulgados posteriormente.</li>
	</ul>
</li>
</ol>
