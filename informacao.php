<h3>Processo seletivo 2008/2</h3>
<p>A seleção é constituída de três etapas que visam à avaliação das aptidões do candidato para integrar a AIESEC, 
sendo elas, respectivamente, a palestra informativa, a entrevista  e o treinamento para novos membros.
</p>
<h3>Regulamento</h3>
<ol>
<li>Pré-requisitos
	<ul class="explicacao">
	<li>Para participar do Processo Seletivo, são necessários ao candidato:
		<ul class="explicacao">
		<li>estar matriculado ou ter concluído o curso de graduação ou qualquer nível superior à graduação há, 
		no máximo, 2 (dois) anos, considerando a data de abertura das inscrições;</li>
		<li>ter realizado a inscrição no período estipulado;</li>
		</ul>
	</li>
	</ul>
</li>
<li>Inscrições
	<ul class="explicacao">
	<li>As inscrições serão feitas mediante cadastro online e participação obrigatória na palestra informativa;</li>
	<li>Não serão aceitas inscrições que excedam o período determinado;</li>
	</ul>
</li>
<li>Etapas
	<ul class="explicacao">
	<li>Como já foi especificado, o Processo Seletivo será constituído por 3 (três) etapas. As datas estão na página 
	principal do processo seletivo, no final da desta, e os locais serão divulgados à medida que forem confirmados 
	com a devida antecedência. As estapas do processo seletivo são:
		<ol class="etapas">
		<li>Palestra informativa: Todos os candidatos deverão assistir a palestra informativa do Processo Seletivo, 
		que serão promovidas pela AIESEC Santa Maria ao longo do período de recrutamento.</li>
		<li>Entrevista (Review Board): Os candidatos aprovados na etapa anterior alcançam a 3ª etapa e estarão na 
		fase decisiva do Processo Seletivo. Realizado por um corpo avaliador, o Review Board é a etapa que determina 
		quais dos candidatos serão definitivamente selecionados. A entrevista terá como foco diversos aspectos 
		individuais observados durante todo o processo, para uma análise mais criteriosa.</li>
		<li>Treinamento de novos membros: Todos os candidatos aprovados nas etapas anteriores participarão do 
		treinamento de novos membros, quando receberão maiores informações sobre a AIESEC em Santa Maria e no mundo, 
		bem como entenderão melhor seu papel dentro da organização. A ausência do candidato é eliminatória.</li>
		</ol>
	</li>
	</ul>
</li>
<li>Eliminação:
	<ul class="explicacao">
	<li>Será considerado eliminado do Processo Seletivo 2008/2 o candidato que:
		<ul class="explicacao">
		<li>Fizer o cadastro online, preencher os seus dados mas esquecer de levar no dia da entrevista a sua ficha 
		impressa com foto 3x4 ou digitalizada;</li>
		<li>Não pagar a taxa de inscrição para a entrevista;</li>
		<li>Não for aprovado nas etapas das quais tenha participado;</li>
		<li>Por quaisquer motivos não comparecerem a uma das etapas do processo, caracterizando-se como desistência 
		(inclusive nas palestras).</li>
		<li>Por quaisquer motivos não chegarem pontualmente aos locais aonde irão ocorrer as etapas, caracterizando-se 
		também como desistência.</li>
		</ul>
	</li>
	</ul>
</li>
<li>Resultados:
	<ul class="explicacao">
	<li>A AIESEC em Santa Maria informará o local e horário das etapas deste processo seletivo, bem como o resultado 
	aos candidatos aprovados em cada etapa, via e-mail cadastrado, bem como pela página de acesso do usuário.</li>
	<li>Os dias e locais das etapas variam, em caso de dúvida acesse a página principal do Processo Seletivo.</li>
	<li>Os locais e horários serão confirmados com antecedência aos candidatos aprovados, de acordo com as etapas.</li>
	<li>Caso alguma data seja alterada, por motivo extraordinário, os candidatos serão notificados com antecedência.</li>
	<li>O valor e o local do treinamento de novos membros serão divulgados posteriormente.</li>
	</ul>
</li>
</ol>
